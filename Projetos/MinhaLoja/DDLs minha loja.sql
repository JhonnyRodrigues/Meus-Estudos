/* Trigger: ao movimentar um produto, atualiza automaticamente o saldo na tabela ESTOQUES */
CREATE TRIGGER TRG_MOVIMENTACAO_ESTOQUE
BEFORE INSERT ON ESTOQUE_MOVIMENTACOES
FOR EACH ROW
BEGIN
    DECLARE V_QTD_ATUAL INT DEFAULT 0;
    DECLARE V_ATIVO CHAR(1);
    -- VERIFICA SE O PRODUTO EXISTE E EST√Å ATIVO
    SELECT QUANTIDADE_ATUAL, ATIVO
    INTO V_QTD_ATUAL, V_ATIVO
    FROM ESTOQUES
    INNER JOIN PRODUTOS ON PRODUTOS.ID = ESTOQUES.FK_PRODUTO
    WHERE FK_PRODUTO = NEW.FK_PRODUTO
    LIMIT 1;
    -- SE PRODUTO ESTIVER INATIVO, BLOQUEIA A MOVIMENTA√á√ÉO
    IF V_ATIVO = 'N' THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'PRODUTO INATIVO, MOVIMENTA√á√ÉO N√ÉO PERMITIDA';
    END IF;
    -- VALIDA√á√ÉO: N√ÉO PERMITIR SALDO NEGATIVO
    IF (V_QTD_ATUAL + NEW.QUANTIDADE) < 0 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'ESTOQUE INSUFICIENTE PARA REALIZAR A OPERA√á√ÉO';
    END IF;
    -- ATUALIZA ESTOQUE DE FORMA AT√îMICA
    UPDATE ESTOQUES 
    SET QUANTIDADE_ATUAL = QUANTIDADE_ATUAL + NEW.QUANTIDADE
    WHERE FK_PRODUTO = NEW.FK_PRODUTO;
    -- SE N√ÉO EXISTIR REGISTRO EM ESTOQUES, CRIA COM QUANTIDADE DA MOVIMENTA√á√ÉO
    IF ROW_COUNT() = 0 THEN
        INSERT INTO ESTOQUES (FK_PRODUTO, QUANTIDADE_ATUAL)
        VALUES (NEW.FK_PRODUTO, NEW.QUANTIDADE);
    END IF;
END;

